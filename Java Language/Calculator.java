public class Calculator{

	private int no1;
	private int no2;

	public Calculator(){

		no1 = 20;
		no2 = 10;

	}

	public int addition(){

		return no1 + no2;
	}

	public int substraction(){

		return no1 - no2;
	}


	public int division(){

		return no1 / no2;
	}

	public int multiplication(){

		return no1 * no2;
	}
}